/*******************************************************************************************************
 * 関数名	func_DataInsert
 * 概要		idからキャラのデータを取得
 * I/O		id	: キャラのID
 *			data	: 取得するデータの種類
 * return	キャラのデータ
 * 更新日	2025/09/20	新規
 *******************************************************************************************************/
function fnc_DB_Get_CharacterInfo(id, data) {
    let retName = "-";

    for(let idx = 0; idx < DB_Characters_Info.length; idx++) {
        if(DB_Characters_Info[idx][enum_DB_Characters.ID] == id) {
            retName = DB_Characters_Info[idx][data];
            break;
        }
    }

    return retName;
}
